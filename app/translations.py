def t(key, **kwargs):
    """Translate a key using the current session language."""
    lang = session.get('lang', 'en')
    text = TRANSLATIONS.get(lang, TRANSLATIONS['en']).get(key)
    if text is None:
        text = TRANSLATIONS['en'].get(key, key)
    if kwargs:
        text = text.format(**kwargs)
    return text
